# if / elif     / else (else y elif dependen de if)
# si / si no si / si no

condicion1 =  False
condicion2 =  False
condicion3 =  True
condicion4 =  False

if condicion1:
    print('Codigo para condicion1')
elif condicion2:
    print('Codigo para condicion2')
elif condicion3:
    print('Codigo para condicion3')
elif condicion4:
    print('Codigo para condicion4')
else:
    print('Ninguna condicion fue cumplida')

